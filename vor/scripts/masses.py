def precision(mass):
    return len(str(mass)[int(math.log10(mass))+2:])

def equal(mass1,mass2):
    prec1 = precision(mass1)
    prec2 = precision(mass2)
    p = min(prec1,prec2)
    m1 = round(mass1,p)
    m2 = round(mass2,p)
